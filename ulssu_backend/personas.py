def get_personas(count: int, start: int = 0) -> list[tuple[str, str]]:
    """`start` 오프셋부터 `count`개의 페르소나를 풀을 순환하며 반환."""
    if count <= 0:
        return []
    pool = PERSONA_POOL
    return [pool[(start + i) % len(pool)] for i in range(count)]
